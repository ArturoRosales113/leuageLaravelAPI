function Functiondos() {
    var elements = document.getElementsByClassName("itemHide"),
        n = elements.length;
    for (var i = 0; i < n; i++) {
      var e = elements[i];
 
      if(e.style.display == 'none') {
        e.style.display = 'block';
      } else {
        e.style.display = 'none';
      }
   }
   
   var elements = document.getElementsByClassName("logoHide"),
        n = elements.length;
    for (var i = 0; i < n; i++) {
      var e = elements[i];
 
      if(e.style.display == 'none') {
        e.style.display = 'block';
      } else {
        e.style.display = 'none';
      }
   }

   var elements = document.getElementsByClassName("logoShow"),
        n = elements.length;
    for (var i = 0; i < n; i++) {
      var e = elements[i];
 
      if(e.style.display == 'block') {
        e.style.display = 'none';
      } else {
        e.style.display = 'block';
      }
   }

   var elements = document.getElementsByClassName("navbar-vertical"),
        n = elements.length;
    for (var i = 0; i < n; i++) {
      var e = elements[i];
 
      if(e.style.width == 'auto') {
        e.style.width = '100%';
      } else {
        e.style.width = 'auto';
      }
   }
   var elements = document.getElementsByClassName("main-content"),
        n = elements.length;
    for (var i = 0; i < n; i++) {
      var e = elements[i];
 
      if(e.style.width == '100%') {
        e.style.width = '100%';
      } else {
        e.style.width = '100%';
      }
   }

 }

